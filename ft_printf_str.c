#include "printf.h"

int	ft_printf_str(char *s)
{
	size_t	i;

	i = 0;
	if (s == NULL)
	{
		ft_putstr_fd("(null)", 1);
		return (6);
	}
	while (s[i] != '\0')
	{
		ft_printf_char(s[i]);
		i++;
	}
	return (i);
}
